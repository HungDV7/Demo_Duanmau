    public KhachHang_model getKH() {
        SimpleDateFormat fm = new SimpleDateFormat("yyyy-MM-dd");
        try {
            return new KhachHang_model(txtMaKH.getText(), txtNameKH.getText(), fm.parse(txtDateKH.getText()), txtSDT.getText(), txtDiaChi.getText(), txtThanhPho.getText(), txtQuocGia.getText(), txtPassKH.getText());
        } catch (ParseException ex) {
            return null;
        }
    }

    public void fillcboCV() {
        listCV = CV_SV.getAllChucVu();
        cboChucVu.removeAllItems();
        for (ChucVu_View chucVu_View : listCV) {
            cboChucVu.addItem(chucVu_View.toString());
        }
    }

    public void fillcboCH() {
        listCH = CH_SV.getAllCH();
        cboCuaHang.removeAllItems();
        for (CuaHang_View chucVu_View : listCH) {
            cboCuaHang.addItem(chucVu_View.toString());
        }
    }

    public NhanVien_Model getNhanVien() {
        SimpleDateFormat fm = new SimpleDateFormat("yyyy-MM-dd");
        String maCH = cboCuaHang.getSelectedItem().toString().split(" ")[0];
        CuaHang_Model ch = CH_SV.getCHByMa(maCH);
        String maCV = cboChucVu.getSelectedItem().toString().split(" ")[0];
        ChucVu_model cv = CV_SV.getChucVuByMa(maCV);
        String gioitinh = "";
        if (rdoNam.isSelected() == true) {
            gioitinh = "Nam";
        } else {
            gioitinh = "Nữ";
        }
        int tt = 0;
        if (rdoDiLam.isSelected() == true) {
            tt = 1;
        }
        NhanVien_Model nv;
        try {
            nv = new NhanVien_Model(txtMaNV.getText(), txtNameNV.getText(), gioitinh, fm.parse(txtNgaySinhNV.getText()), txtDiaChiNV.getText(), txtSDTNV.getText(), txtPassNV.getText(), ch, cv, tt);
            return nv;

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ngay Sinh không đúng định dạng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }
