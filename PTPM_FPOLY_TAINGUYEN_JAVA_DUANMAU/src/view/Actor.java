    public void show(int index) {
        if (tab.getSelectedIndex() == 0) {
            NhanVien_Model nv = NV_SV.getNVByMa(listNV.get(index).getMa());
            txtMaNV.setText(nv.getMa());
            txtNameNV.setText(nv.getTen());
            txtPassNV.setText(nv.getMatKhau());
            txtNgaySinhNV.setText(nv.getNgaySinh() + "");
            txtSDTNV.setText(nv.getSdt());
            txtDiaChiNV.setText(nv.getDiaChi());
            cboChucVu.setSelectedItem(nv.getChucVu().toString());
            cboCuaHang.setSelectedItem(nv.getCuaHang().toString());
            if (nv.getGioiTinh().equalsIgnoreCase("Nam")) {
                rdoNam.setSelected(true);
            } else {
                rdoNu.setSelected(true);
            }
            if (nv.getTrangThai() == 0) {
                rdoONhan.setSelected(true);
            } else {
                rdoDiLam.setSelected(true);
            }
        } else if (tab.getSelectedIndex() == 1) {
            KhachHang_model kh = KH_SV.getKHByMa(listKH.get(index).getMa());
            txtMaKH.setText(kh.getMa());
            txtPassKH.setText(kh.getPassword());
            txtSDT.setText(kh.getSdt());
            txtNameKH.setText(kh.getHoTen());
            txtDateKH.setText(kh.getNgaySinh() + "");
            txtDiaChi.setText(kh.getDiaChi());
            txtThanhPho.setText(kh.getThanhPho());
            txtQuocGia.setText(kh.getQuocGia());
        } else if (tab.getSelectedIndex() == 2) {
            CuaHang_Model ch = CH_SV.getCHByMa(listCH.get(index).getMa());
            txtMaCH.setText(ch.getMa());
            txtNameCH.setText(ch.getTen());
            txtDiaChiCH.setText(ch.getDiaChi());
            txtThanhPhoCH.setText(ch.getThanhPho());
            txtQuocGiaCH.setText(ch.getQuocGia());
        } else {
            ChucVu_model cv = CV_SV.getChucVuByMa(listCV.get(index).getMa());
            txtMaCV.setText(cv.getMa());
            txtTenCV.setText(cv.getTen());
        }
    }

    public boolean chk() {
        if (tab.getSelectedIndex() == 0) {
            if (Chk.chknull("Không để trống mã NV", txtMaNV)) {
                return false;
            } else if (Chk.chknull("Không để trống Password", txtPassNV)) {
                return false;
            } else if (Chk.chknull("Không để trống Họ tên Nhân viên", txtNameNV)) {
                return false;
            } else if (Chk.chknull("Không để trống Ngày Sinh", txtNgaySinhNV)) {
                return false;
            } else if (txtNameNV.getText().split(" ").length < 3) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ họ tên", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return false;
            } else {
                return true;
            }
        } else if (tab.getSelectedIndex() == 1) {
            if (Chk.chknull("Không để trống mã KH", txtMaKH)) {
                return false;
            } else if (Chk.chknull("Không để trống Password", txtPassKH)) {
                return false;
            } else if (Chk.chknull("Không để trống họ tên", txtNameKH)) {
                return false;
            } else if (Chk.chknull("Không để trống ngày sinh", txtDateKH)) {
                return false;
            } else if (Chk.chkDate("Ngày Sinh không đúng định dạng", txtDateKH)) {
                return false;
            } else {
                return true;
            }
        } else if (tab.getSelectedIndex() == 2) {
            if (Chk.chknull("Không để trống mã cửa hàng", txtMaCH)) {
                return false;
            } else if (Chk.chknull("Không để trống Tên cửa hàng", txtNameCH)) {
                return false;
            } else {
                return true;
            }
        } else {
            if (Chk.chknull("Không để trống mã Chức Vụ", txtMaCV)) {
                return false;
            } else if (Chk.chknull("Không để trống Tên Chức Vụ", txtTenCV)) {
                return false;
            } else {
                return true;
            }
        }
    }

