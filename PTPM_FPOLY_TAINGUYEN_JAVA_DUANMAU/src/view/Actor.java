    public void filltable() {
        if (tab.getSelectedIndex() == 0) {
            fillcboCV();
            fillcboCH();
            listNV = NV_SV.getALllNV();
            mol = (DefaultTableModel) tblNV.getModel();
            mol.setRowCount(0);
            String trangThai = "";
            for (NhanVien_View n : listNV) {
                if (n.getTrangThai() == 0) {
                    trangThai = "Ở Nhà";
                } else {
                    trangThai = "Đi làm";
                }
                mol.addRow(new Object[]{n.getStt(), n.getMa(), n.getTen(), n.getGioiTinh(), n.getNgaySinh(), n.getChucVu(), n.getCuaHang(), trangThai});
            }
        } else if (tab.getSelectedIndex() == 1) {
            listKH = KH_SV.getAllKH();
            mol = (DefaultTableModel) tblKH.getModel();
            mol.setRowCount(0);
            for (KhachHang_view k : listKH) {
                mol.addRow(new Object[]{k.getStt(), k.getMa(), k.getHoTen(), k.getNgaySinh(), k.getDiaChi(), k.getThanhPho(), k.getQuocGia()});
            }
        } else if (tab.getSelectedIndex() == 2) {
            listCH = CH_SV.getAllCH();
            mol = (DefaultTableModel) tblCH.getModel();
            mol.setRowCount(0);
            for (CuaHang_View c : listCH) {
                mol.addRow(new Object[]{c.getSTT(), c.getMa(), c.getTen(), c.getDiaChi(), c.getThanhPho(), c.getQuocGia()});
            }
        } else {
            listCV = CV_SV.getAllChucVu();
            mol = (DefaultTableModel) TblCV.getModel();
            mol.setRowCount(0);
            for (ChucVu_View chucVu_View : listCV) {
                mol.addRow(new Object[]{chucVu_View.getStt(), chucVu_View.getMa(), chucVu_View.getTen()});
            }
        }
    }

