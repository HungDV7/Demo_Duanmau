    public boolean chkValidate() {
        if (Tab.getSelectedIndex() == 0) {
            if (Chk.chknull("Không Để Trống Mã Sản Phẩm", txtMaSP)) {
                return false;
            } else if (Chk.chknull("Vui Lòng Nhập Tên Sản Phẩm", txtTenSP)) {
                return false;
            } else {
                return true;
            }
        } else if (Tab.getSelectedIndex() == 1) {
            if (Chk.chknull("Vui Lòng Chọn Sản Phẩm", txtIDSP)) {
                return false;
            } else if (Chk.chknull("Không Để Trống Năm Bảo Hành", txtNamBH)) {
                return false;
            } else if (Chk.chkFloat("Năm Bảo Hành Phải là Số", "Năm Bảo Hành Phải >=0", txtNamBH)) {
                return false;
            } else if (Chk.chknull("Không Để Trống Số Lượng", txtSL)) {
                return false;
            } else if (Chk.chkInt("Số Lượng Phải là Số", "Năm Bảo Hành Phải >0", txtSL)) {
                return false;
            } else if (Chk.chknull("Không Để Trống Giá Nhập", txtGiaNhap)) {
                return false;
            } else if (Chk.chkFloat("Giá Nhập Phải là Số", "Giá Nhập Phải >=0", txtGiaNhap)) {
                return false;
            } else if (Chk.chknull("Không Để Trống Giá Bán", txtGiaBan)) {
                return false;
            } else if (Chk.chkFloat("Giá Bán Phải là Số", "Giá Nhập Phải >=0", txtGiaBan)) {
                return false;
            } else {
                return true;
            }
        } else if (Tab.getSelectedIndex() == 2) {
            if (Chk.chknull("Không Để Trống Mã Màu", txtMaSP1)) {
                return false;
            } else if (Chk.chknull("Vui Lòng Nhập Tên Màu", txtTenSP1)) {
                return false;
            } else {
                return true;
            }
        } else if (Tab.getSelectedIndex() == 3) {
            if (Chk.chknull("Không Để Trống Mã NSX", txtMaNSX)) {
                return false;
            } else if (Chk.chknull("Vui Lòng Nhập Tên NSX", txtTenNSX)) {
                return false;
            } else {
                return true;
            }
        } else {
            if (Chk.chknull("Không Để Trống Mã Dòng SP", txtmaDongSP)) {
                return false;
            } else if (Chk.chknull("Vui Lòng Nhập Tên Dòng SP", txtTenDongSP)) {
                return false;
            } else {
                return true;
            }
        }
    }
