    public void show(int index) {
        if (Tab.getSelectedIndex() == 0) {
            //Show SP
            SanPham_Model s = SanPham_SV.getSPByMa(listSP.get(index).getMa());
            txtMaSP.setText(s.getMa());
            txtTenSP.setText(s.getTen());
        } else if (Tab.getSelectedIndex() == 1) {
            //Show Chi Tiet SP
            ChiTietSP_Model CTSP = CTSP_Service.getCTSPByid(listCTSP.get(index).getId());
            SanPham_Model sp = SanPham_SV.getSPByMa(listCTSP.get(index).getMasanPham());
            txtIDSP.setText(sp.getId());
            txtMa.setText(sp.getMa());
            txtTen.setText(sp.getTen());
            cboNSX.setSelectedItem(CTSP.getNsx().toString());
            cboMau.setSelectedItem(CTSP.getMauSac().toString());
            cboDongSP.setSelectedItem(CTSP.getDongSP().toString());
            txtNamBH.setText(CTSP.getNamBH() + "");
            txtSL.setText(CTSP.getSlTon() + "");
            txtGiaNhap.setText(CTSP.getGiaNhap() + "");
            txtGiaBan.setText(CTSP.getGiaBan() + "");
            txtMota.setText(CTSP.getMoTa());
        } else if (Tab.getSelectedIndex() == 2) {
            //Show Mau
            MauSac_Model m = MauSac_SV.getByMa(listMS.get(index).getMa());
            txtMaSP1.setText(m.getMa());
            txtTenSP1.setText(m.getTen());
        } else if (Tab.getSelectedIndex() == 3) {
            //show NSX
            NSX_Model n = NSX_SV.getByMa(listNSX.get(index).getMa());
            txtMaNSX.setText(n.getMa());
            txtTenNSX.setText(n.getTen());
        } else if (Tab.getSelectedIndex() == 4) {
            //Show Dong SP
            DongSP_Model d = DongSP_SV.getByMa(listDongSP.get(index).getMa());
            txtmaDongSP.setText(d.getMa());
            txtTenDongSP.setText(d.getTen());
        }
    }

    public void clear() {
        if (Tab.getSelectedIndex() == 0) {
            //Clear SP
            String ma = listSP.get(listSP.size() - 1).getMa().substring(2, listSP.get(listSP.size() - 1).getMa().length());
            txtMaSP.setText("SP" + (Integer.parseInt(ma) + 1));
            txtTenSP.setText("");
        } else if (Tab.getSelectedIndex() == 1) {
            //Clear SP
            cboNSX.setSelectedIndex(0);
            cboDongSP.setSelectedIndex(0);
            cboMau.setSelectedIndex(0);
            txtNamBH.setText("");
            txtSL.setText("");
            txtGiaNhap.setText("");
            txtGiaBan.setText("");
        } else if (Tab.getSelectedIndex() == 2) {
            //Clear Mau 
            txtMaSP1.setText("");
            txtTenSP1.setText("");
        } else if (Tab.getSelectedIndex() == 3) {
            //Clear NSX 
            String ma = listNSX.get(listNSX.size() - 1).getMa().substring(3, listNSX.get(listNSX.size() - 1).getMa().length());
            txtMaNSX.setText("NSX" + (Integer.parseInt(ma) + 1));
            txtTenNSX.setText("");
        } else if (Tab.getSelectedIndex() == 4) {
            //Clear Dong
            String ma = listDongSP.get(listDongSP.size() - 1).getMa().substring(3, listDongSP.get(listDongSP.size() - 1).getMa().length());
            txtmaDongSP.setText("DSP" + (Integer.parseInt(ma) + 1));
            txtTenDongSP.setText("");
        }
    }

