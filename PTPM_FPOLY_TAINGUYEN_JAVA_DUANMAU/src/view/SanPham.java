        String maNSX = cboNSX.getSelectedItem().toString().split(" ")[0];
        NSX_Model nsx = NSX_SV.getByMa(maNSX);
        //get DongSP 
        String madong = cboDongSP.getSelectedItem().toString().split(" ")[0];
